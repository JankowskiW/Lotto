package pl.wj.lotto.domain.ticket.model.dto;

import jakarta.validation.constraints.Min;
import jakarta.validation.constraints.NotBlank;
import lombok.Builder;

import java.util.List;

@Builder
public record TicketRequestDto(
        String id,
        @NotBlank(message = "{ticket.user-id.not-blank}")
        String userId,
        @Min(value = 1, message = "{ticket.game-type-id.positive-number}")
        int gameTypeId,
        @Min(value = 1, message = "{ticket.draws-amount.positive-number}")
        int drawsAmount,
        boolean numbersAutogenerated,
        int generatedNumbersAmount,
        List<Integer> mainNumbers,
        List<Integer> extraNumbers
        ) {
}
